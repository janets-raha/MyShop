A propos <br><br>
<p>Fervents partisans d'une consommation plus juste et de qualité, nous avons repensé le Court Circuit.

<p>Inscrit dans une action d'économie sociale et solidaire, le fruities.fr apporte une réponse à l'attente première des consommateurs - un bon prix pour un bon produit - et aux producteurs qui voient leurs revenus durement impactés par la multiplication des intermédiaires dans les circuits classiques.
<p>Pourquoi FRUITIES ? </p>

<p>Depuis quelques années, petites et grandes initiatives voient le jour pour répondre à l’engouement du made in « près de chez soi ». Avec FRUITIES, notre volonté est de contribuer à développer et populariser une solution aux nouveaux besoins de consommation : consommer frais, de qualité et local. Fervents partisans d'une consommation plus juste et de qualité, nous avons imaginé FRUITIES, fruit d’un constat sur le terrain : l’accessibilité aux producteurs en vente directe est actuellement insuffisante. </p>

<p>Envie de manger plus frais, plus goûteux, de consommer local, de connaître l’origine des aliments et la façon dont ils sont produits, de se réapproprier son alimentation... </p>

<p>Nombreuses sont les raisons qui poussent les consommateurs à chercher de nouvelles façons d’acheter. Quant aux producteurs proposant leurs produits en vente directe, ils souffrent encore aujourd’hui cruellement d’un manque de visibilité et d’accessibilité soutenue par des outils technologiques adaptés. </p>

<p>Inscrit dans une action d'économie sociale et solidaire, FRUITIES apporte une réponse à l'attente première des consommateurs - un bon prix pour un bon produit - et aux producteurs qui voient leurs revenus durement impactés par la multiplication des intermédiaires dans les circuits classiques. </p>

<p>La démarche FRUITIES s’appuie sur ces constats afin d’offrir un service répondant à la fois aux besoins des consommateurs et des producteurs.</p>

